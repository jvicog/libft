#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	t;

	t = 0;
	if (s != NULL)
	{
		while (s[t])
		{
			f(t, &s[t]);
			t++;
		}
	}
}
